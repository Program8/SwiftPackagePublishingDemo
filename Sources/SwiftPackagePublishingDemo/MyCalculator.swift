public class MyCalculator {
    public init() {}
    /// Added in 1.0.0
    public func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    /// Added in 1.0.1
    public func multiply(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
    
}
